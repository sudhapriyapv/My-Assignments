package week1.day2;

public class OddOrEven {

	public static void main(String[] args) {
		 
		//int x = 15;
		for (int x=10;x<=20;x++) {
		if (x%2 == 0) {
			System.out.println("The number " + x + " is a Even number");
		} else {
			System.out.println("The number " + x + " is a Odd number");
		}
		}
	}
 
}
